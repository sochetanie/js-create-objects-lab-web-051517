'use strict';

function Dog(name, breed, age) {
    this.name = name
    this.breed = breed
    this.age = age
}

function Cat(name, breed, age) {
    this.name = name
    this.breed = breed
    this.age = age
}

function Ferret(name, age, x) {
    this.name = name
    this.foodRequirements = x
    this.age = age
}

function Bird(name, breed) {
    this.name = name
    this.breed = breed
}

function Fish(name, x, y) {
    this.name = name
    this.species = x
    this.waterConditions = y
}